
package aula3info;

public class CarroDetalhe {

    /**
     * @return the carro
     */
    public String getCarro() {
        return carro;
    }

    /**
     * @param carro the carro to set
     */
    public void setCarro(String carro) {
        this.carro = carro;
    }

    /**
     * @return the valorsolicitado
     */
    public Float getValorsolicitado() {
        return valorsolicitado;
    }

    /**
     * @param valorsolicitado the valorsolicitado to set
     */
    public void setValorsolicitado(Float valorsolicitado) {
        this.valorsolicitado = valorsolicitado;
    }

    /**
     * @return the placa
     */
    public String getPlaca() {
        return placa;
    }

    /**
     * @param placa the placa to set
     */
    public void setPlaca(String placa) {
        this.placa = placa;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }
    private String carro;
    private Float valorsolicitado;
    private String placa;
    private String nome;

}
